//Even numbers  using  For loop


public class ForLoop {
	
	public static void main(String[]args) {
			
			int i=0;		//initialize from
			
			for(int j=1;j<=10;j++) {
			i=i+j;	//logic
			}
			
			System.out.println("First 10 even numbers is: \n"+i);
			}
}	
