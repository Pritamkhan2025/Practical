	//Even number  using  while loop


public class Whileloop {
	
		public static void main(String[]args) {
		
				int i=0;		//initialize from
				System.out.println("First 10 even numbers  \n");
			while(i<=10) {		// given range
				System.out.println(i);
				i=i+2;			//logic
	}
	}
	}

