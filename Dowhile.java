//Even numbers using  do while loop


public class Dowhile {
	public static void main(String[]args) {
		
		int i=0;		//initialize from
		System.out.println("First 10 even numbers  \n");
		do {
			System.out.println(i);
			i=i+2;		//logic
		}
		while(i<=10);   //given range
			
	}
}
