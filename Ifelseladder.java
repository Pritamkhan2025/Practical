//Print name using ffelse ladder


public class Ifelseladder {
	public static void main(String[]args) {
		String name ="Pritam";
		if(name == "Arnab") {
			System.out.println("name is Arnab");
		}else if(name =="Sourav") {
			System.out.println("name is Sourav");
		}else if(name =="Raj") {
			System.out.println("name is Raj");
		}else {
			System.out.println(name);
		}
	}
}
